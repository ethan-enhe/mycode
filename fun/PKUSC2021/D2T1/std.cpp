const ll MXN = 1e6 + 5;

ll n, c;
vector<ll> g[MXN];
mod sz[MXN], subt_s[MXN], subt_s2[MXN], ans0, ans1;

void dfs(ll p) {
    subt_s[p] = subt_s2[p] = 0;
    sz[p] = 1;
    for (ll nx : g[p]) {
        dfs(nx);
        sz[p] += sz[nx];
        subt_s[p] += subt_s[nx];
        subt_s2[p] += subt_s2[nx];
    }
    subt_s[p] += sz[p], subt_s2[p] += sz[p] * sz[p];
}
void cal(ll p, mod anc_s, mod anc_s2, mod dpth) {
    for (ll nx : g[p]) cal(nx, anc_s + sz[p], anc_s2 + sz[p] * sz[p], dpth + 1);
    ans0 += sz[p] * ((mod)n - sz[p]);
    ans1 += sz[p] * ((mod)n - sz[p]) * sz[p] * ((mod)n - sz[p]);
    ans1 += ((mod)n - sz[p]) * ((mod)n * 2 - sz[p]) * (sz[p] * (subt_s[p] - sz[p]) - (subt_s2[p] - sz[p] * sz[p]));
    ans1 += sz[p] * (sz[p] + n) * (anc_s * (sz[p] + n) - sz[p] * n * dpth - anc_s2);
    ans1 +=
        sz[p] * (sz[p] + n) * (((mod)n - sz[p]) * (subt_s[1] - subt_s[p] - anc_s) - (subt_s2[1] - subt_s2[p] - anc_s2));
    /* cerr << p << " " << ans1 << nl; */
}

