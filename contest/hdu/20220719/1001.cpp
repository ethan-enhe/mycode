const char nl = '\n';
const ll INF = 1e18;
const ll MXN = 1e6 + 5;

ll n, m, arr[MXN], pre[MXN];
char str[MXN];
ll find(int x, char y) {
    while (1) {
        if (str[x + 1] == y)
            return x + 1;
        else if (!x)
            return 0;
        else
            x = pre[x];
    }
}
vector<ll> g[MXN];
vector<ll> q[MXN];
ll top[MXN], ans;
stack<pi> stk;

int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--) {
        ans = 1;
        cin >> (str + 1);
        n = strlen(str + 1);
        cin >> m;

        for (ll i = 2; i <= n; i++) pre[i] = find(pre[i - 1], str[i]);
        for (ll i = 0; i <= n; i++) {
            g[i].clear();
            q[i].resize(1);
            top[i] = 0;
        }
        for (ll i = 0; i <= n; i++) {
            if (i) g[pre[i]].push_back(i);
            q[i * 2 % m].push_back(0);
        }

        stk.push({0, 0});
        while (!stk.empty()) {
            ll p = stk.top().fi;
            ll &cure = stk.top().se;

            if (cure == 0) {
                ll curmod = p * 2 % m;
                q[curmod][++top[curmod]] = p;

                ll lastmod = p % m;
                ll id = lower_bound(q[lastmod].begin() + 1, q[lastmod].begin() + 1 + top[lastmod], p / 2 + 1) -
                        q[lastmod].begin();
                ans = ans * (top[lastmod] - id + 2) % P;
            }
            if (cure < g[p].size()) {
                stk.push({g[p][cure], 0});
                ++cure;
                continue;
            } else {
                ll curmod = p * 2 % m;
                --top[curmod];
                stk.pop();
            }
        }
        cout << ans << nl;
    }
    return 0;
}
