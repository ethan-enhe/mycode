const ll P = 1e9 + 7;
const char nl = '\n';
const ll INF = 1e9;
const ll MXN = 20000 + 5;
const ll MXM = 2005;

ll n, m, t;
ll arr[MXN];
ll dp[MXM][MXM];
int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    cin >> t;
    while (t--) {
        cin >> n >> m;
        for (ll i = 1; i <= n; i++) cin >> arr[i];
        for (ll i = 0; i <= m; i++)
            for (ll j = 0; j <= m; j++) dp[i][j] = INF;
        ll ans = INF;
        for (ll i = 1; i <= n; i++) {
            ll ti = i % m;
            for (ll j = 0; j <= m; j++) dp[ti][j] = INF;
            if (i <= m)
                for (ll j = 1; j <= m - 1; j++) dp[ti][j] = arr[i] + arr[i - j];
            for (ll j = 1; j <= m - 1 && j <= i - 1; j++) {
                ll tmp = ti - j;
                if (tmp < 0) tmp += m;
                dp[ti][j] = min({dp[ti][j], dp[ti][j - 1], arr[i] + dp[tmp][m - j]});
                if (i - j + m > n) umn(ans, dp[ti][j]);
            }
        }
        cout << ans << nl;
    }
    return 0;
}
