const char nl = '\n';
const ll INF = 1e18;
const ll MXN = 2e5 + 5;

ll P, a, n, q;
unordered_map<ll, ll> place;
ll s[MXN], d[MXN];
ll qpow(ll x, ll y) {
    ll r(1);
    while (y) {
        if (y & 1) r = r * x % P;
        x = x * x % P, y >>= 1;
    }
    return r;
}
int main() {
    ios::sync_with_stdio(0);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--) {
        cin >> P >> a >> n >> q;
        for (ll i = 1; i <= n; i++) {
            cin >> s[i] >> d[i];
            s[i] = qpow(s[i], P - 2);
        }
        place.clear();
        for (ll i = 0, v = 1; i <= MXN; i++, v = v * a % P) {
            if (place.count(v)) break;
            place[v] = i;
        }
        for (ll i = 1, x; i <= q; i++) {
            cin >> x;
            ll ans = 0;
            for (ll j = 1; j <= n; j++) {
                if (x == 0)
                    ans += s[j] == 0;
                else if (s[j]) {
                    ll tmp = s[j] * x % P;
                    auto it = place.find(tmp);
                    if (it != place.end()) ans += it->se <= d[j];
                }
            }
            cout << ans << nl;
        }
    }
    return 0;
}
