ll cnt(ll x, ll y) {
    // cout << x << " " << y << endl;
    if (x == 0) return 0;
    if (y == 0) return 1;
    if (x == y) return 2;
    if (x >= y) {
        ll div = (x / y);
        if (div & 1)
            return cnt(y, x % y) + ((div - 1) / 2) * 3 + 1;
        else
            return cnt(x % y, y) + (div / 2) * 3;
    } else {
        ll div = (y / x);
        if (div & 1)
            return cnt(y % x, x) + ((div - 1) / 2) * 3 + 2;
        else
            return cnt(x, y % x) + (div / 2) * 3;
    }
}

