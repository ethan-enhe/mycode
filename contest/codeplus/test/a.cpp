    for (ll i = 1; i < n; i++) {
        for (ll j = 1; j < 3; j++) {
            dp[i + 1][j + 1] += dp[i][j];
            dp[i + 1][1] += dp[i][j];
            dp[i + 1][1] += dp[i][j];
        }
    }
