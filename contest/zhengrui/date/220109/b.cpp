bool chk(ll x){
	ll cnt=0;
	if((x&3)==2 || (x&3)==1)return 0;
	if((x&7)==0 && (x&15))return 0;
	for(ll j=1+(!(x&1));j*j<=x;j+=2)
		if(x%j==0){
			ll i=j,nx=x/i;
			if(!((i+nx)&3)){
				cnt+=(3*i*i-4*i>=x);
				if(i!=nx)cnt+=(3*nx*nx-4*nx>=x);
				if(cnt>=2)return 0;
			}
		}
	return cnt==1;
}
