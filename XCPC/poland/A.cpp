void ghash(string &s, ull &h0, ull &_h0, ll len) {
    for (ll i = 0; i < min(ll(s.size()), len); i++) {
        h0 = (h0 * B + s[i]) % M;
        _h0 = (_h0 * _B + s[i]) % _M;
    }
}

